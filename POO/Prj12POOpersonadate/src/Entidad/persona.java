package Entidad;

import java.util.Date;

/**
 *
 * @author Equipo
 */
public class persona {

    private String nombre;
    private Date fechaNac;
    private int edad;
    private int mes;
    private int anio;
    private int dia;

    public persona() {
    }

    public persona(String nombre, Date fechaNac, int edad, int mes, int anio, int dia) {
        this.nombre = nombre;
        this.fechaNac = fechaNac;
        this.edad = edad;
        this.mes = mes;
        this.anio = anio;
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Date getFechaNac() {
        return fechaNac;
    }

    public void setFechaNac(Date fechaNac) {
        this.fechaNac = fechaNac;
    }

}
